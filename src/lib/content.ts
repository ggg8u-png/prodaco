// FNV-1a 해시 — 비슷한 슬러그도 전혀 다른 시드로 흩어지게 한다.
function seedOf(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}
// 시드 + 섹션별 salt 로 독립적인 인덱스를 뽑는다.
function idx(seed: number, salt: number, len: number): number {
  const x = (Math.imul(seed ^ (salt * 0x9e3779b1), 2654435761) >>> 0);
  return x % len;
}
function pick<T>(seed: number, salt: number, arr: T[]): T {
  return arr[idx(seed, salt, arr.length)];
}
// 시드 기반 결정적 셔플(Fisher–Yates) — 순서 다양화.
function shuffle<T>(seed: number, arr: T[]): T[] {
  const a = arr.slice();
  let s = seed >>> 0;
  for (let i = a.length - 1; i > 0; i--) {
    s = (Math.imul(s, 1103515245) + 12345) >>> 0;
    const j = s % (i + 1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// ─── 품목군 분류 ───────────────────────────────────────────────────────────────
type Family = "maru" | "vinyl" | "tile" | "coating" | "sanding" | "bond" | "generic";
function familyOf(item: string): Family {
  const s = item || "";
  if (/(샌딩|면갈이|마루재생|마루코팅)/.test(s)) return "sanding";
  if (/(에폭시|우레탄)/.test(s)) return "coating";
  if (/본드/.test(s)) return "bond";
  if (/(폴리싱|도기|바닥타일)/.test(s)) return "tile";
  if (/타일/.test(s) && !/(데코|디럭스)/.test(s)) return "tile";
  if (/(데코|디럭스|륨|장판)/.test(s)) return "vinyl";
  if (/마루/.test(s)) return "maru";
  return "generic";
}

