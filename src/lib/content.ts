function regionBlock(k: KeywordEntry, seed: number): string {
  const reg = k.region;
  if (!reg || reg === "수도권") {
    return `${pick(seed, 52, REGION_BROAD)("수도권")}\n\n${regionOps("수도권", seed)}`;
  }
  // 권역별 고유 서술(강남권·인천권 …) 또는 광역(서울/경기/인천) 전용 서술.
  const profileKey = REGION_PROFILE[reg] ? reg : clusterLabelOf(reg);
  const pool = REGION_PROFILE[profileKey];
  const nb = neighborsOf(reg, 3);
  const narrative = pool ? pick(seed, 51, pool)(reg, nb) : pick(seed, 51, REGION_GENERIC)(reg, nb);
  // 지역(구·시)별 실제 성격 한 줄(있으면) → 같은 권역의 지역들도 서로 달라진다.
  const fact = REGION_FACTS[reg] ? `\n\n${REGION_FACTS[reg]}` : "";
  // 권역 서술 + 지역 성격 + 인근 동선 한 줄(지역마다 다른 인접지역).
  return `${narrative}${fact}\n\n${regionOps(reg, seed)}`;
}

