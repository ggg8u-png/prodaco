function familyKeyOf(item: string): keyof typeof FAMILY {
  const s = item || "";
  if (/(샌딩|면갈이|마루재생|마루코팅)/.test(s)) return "sanding";
  if (/(에폭시|우레탄)/.test(s)) return "coating";
  if (/(폴리싱|도기|바닥타일)/.test(s)) return "tile";
  if (/타일/.test(s) && !/(데코|디럭스)/.test(s)) return "tile";
  if (/(데코|디럭스|륨)/.test(s)) return "deco";
  if (/장판/.test(s)) return "jangpan";
  if (/원목마루/.test(s)) return "wonmok";
  if (/(강화마루|합판마루)/.test(s)) return "ganghwa";
  if (/(강마루|온돌마루|마루)/.test(s)) return "ganmaru";
  return "generic";
}

// 품목별 기술 정보(품목 특화 override + 품목군 기본값 병합).
export function itemFactsFor(item: string | undefined): ItemFact {
  const key = item || "바닥재철거";
  const base = FAMILY[familyKeyOf(key)];
  const override = BY_ITEM[key];
  return override ? { ...base, ...override } : base;
}

