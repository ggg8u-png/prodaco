// ─── 조합 결과 ─────────────────────────────────────────────────────────────────
export interface ComboProfile {
  regionLine: string; // 지역별 현장 특성(작업 관점)
  difficulty: string; // 품목별 작업 난이도
  sandingBond: string; // 샌딩/본드 제거 필요성
  photoInfo: string; // 사진 견적 시 필요한 정보
  nextProcess: string; // 다음 공정 전 확인할 점
}

// 키워드(지역×품목)에 맞는 결합 프로파일 — 커버되면 고유 데이터, 아니면 variant 폴백.
export function comboProfileFor(k: KeywordEntry): ComboProfile {
  const seed = seedOf(k.slug);
  const item = k.item || "바닥재 철거";
  const fam = familyOf(item);

  // 지역: 핵심 10개는 고유, 그 외 지역/광역/수도권은 variant 폴백.
  const reg = k.region && k.region !== "수도권" ? k.region : "";
  let regionLine: string;
  if (reg && REGION_SITE[reg]) {
    regionLine = pick(seed, 1, REGION_SITE[reg]);
  } else if (reg) {
    const nb = neighborsOf(reg, 3);
    regionLine = pick(seed, 2, REGION_FALLBACK)(reg, clusterLabelOf(reg), nb);
  } else {
    // 지역이 없는(품목 단독/수도권) 페이지 — 수도권 전역 톤.
    regionLine =
      "서울·경기·인천 수도권 전역을 방문 작업하며, 건물 유형과 주차·엘리베이터 여건을 먼저 확인해 반출 동선을 잡습니다. 정확한 지역과 현장 사진을 주시면 그에 맞춰 안내합니다.";
  }

  // 품목: 핵심 10개는 고유, 그 외는 품목군(family) variant 폴백.
  const ip = ITEM_PROFILE[item];
  const difficulty = ip ? pick(seed, 3, ip.difficulty) : pick(seed, 3, FAMILY_DIFFICULTY[fam]);
  const sandingBond = ip ? pick(seed, 4, ip.sandingBond) : pick(seed, 4, FAMILY_SANDBOND[fam]);
  const photoInfo = pick(seed, 5, FAMILY_PHOTO[fam]);
  const nextProcess = pick(seed, 6, FAMILY_NEXT[fam]);

  return {
    regionLine: applyReplacements(regionLine),
    difficulty: applyReplacements(difficulty),
    sandingBond: applyReplacements(sandingBond),
    photoInfo: applyReplacements(photoInfo),
    nextProcess: applyReplacements(nextProcess),
  };
}
